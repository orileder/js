const countryInfo = (country, population, capitalCity) => {
   return country + ' has ' + population + ' people and its capital is ' + capitalCity;
}
const var1 = countryInfo('italy', 60000000, 'rome');
const var2 = countryInfo('japan', 80000000, 'tokyo');
const var3 = countryInfo('USA', 40000000, 'washington');
 
